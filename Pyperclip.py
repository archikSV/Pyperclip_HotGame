import pyperclip


def main():
    while True:
        # Запрашиваем у пользователя название игры и тип
        game_name = input("Enter the game name: ")
        game_type = input("Enter the game type: ")

        # Формируем текст на основе введенных данных
        text = """Задача:
Написать описание для игры придерживаясь правил.

Переменные (для принятия решений дальше):
НАЗВАНИЕ ИГРЫ = "{}"
ТИП = "{}"

Влияние переменных:
Выше указаны переменные и их значение.
НАЗВАНИЕ ИГРЫ - переменная, значение которой нужно будет подставить в текст в указаном месте.
ТИП - переменная, которая указывает что собой являет данная игра. Это может быть как просто игра, так и дополнение к игре, издание (например deluxe edition), бандл или что-либо другое в этом роде. В зависимости от значение даной переменной, ты должен определить о чем будет написано в описании. Если это оригинальная игра, то в описании должна описыватся сама игра. Если это ДЛС (или же дополнение), то в описании должна описыватся как сама игра, так и дополнение (что оно привносит в игру). Если это бандл, то в описании должен описыватся состав бандла и его общее описание.

Обязательные правила:
- Описание не должно привышать 5-6 предложений.
- В описании обязательно должно присутствывать словосочетание "купить [НАЗВАНИЕ ИГРЫ]", где вместо [НАЗВАНИЕ ИГРЫ] должно быть подставлено значение этой переменной.
- В описании не должна присутствовать следующая информация: издатель, разработчик, год выпуска, места где можно купить игру, магазины, системные требования, платформы.
- Если это возможно, используй steam в качевсте основного источника информации (избегать прямого цитирования). Если невозможно – искать официальные источники.

Примеры хороших описаний:
НАЗВАНИЕ ИГРЫ = "Overpass 2"
ТИП = "Оригинальная игра"
Описание: Любите гонки? Мы тоже любим. Но что мы любим ещё больше – так это гоночные симуляторы по бездорожью, так ещё и с большим количеством препятствий на пути! Воспользуйтесь возможностью купить Overpass 2 и испытайте на себе этот уникальный опыт в мире гоночных игр. Всё выше перечисленное вы будете преодолевать на одной из предложенных игрой багги. В игре представлен широкий выбор этих монстров бездорожья, а также широкий ассортимент модернизаций для них.

НАЗВАНИЕ ИГРЫ = "Deceit 2"
ТИП = "Оригинальная игра"
Описание: Испытайте преданность своих друзей, купив Deceit 2. Выберитесь из смертельной ловушки, спасаясь от межпространственных демонов и стараясь сохранить при этом рассудок. Вместе с вами пытаются добиться своих целей ещё восемь игроков, однако лишь у шести из них стоит задача выбраться и выжить. Остерегайтесь предателей среди вас. Ведь вы не один из них... Верно?

НАЗВАНИЕ ИГРЫ = "Dead Space Remake"
ТИП = "Переиздание"
Описание: Купите Dead Space Remake – переиздании игры Dead Space, и почувствуйте себя в роли инженера на космическом корабле, работа совсем не опасная, вы не воин и не солдат. Но всё может измениться в один миг, и вы должны стать тем, кто защитит оставшихся на корабле людей от оживших мертвецов. Но это не обычные зомби, которых вы видели раньше, они некроморфы, ужасающий гибрид инопланетных существ и зомби.

НАЗВАНИЕ ИГРЫ = "Hearts of Iron IV: Arms Against"
ТИП = "Дополнение"
Описание: Воспользуйтесь возможностью купить Hearts of Iron IV: Arms Against Tyranny DLC и большое количество нового контента в легендарной глобальной стратегии. Дополнение привносит в игру новые фокусы для скандинавских стран, а также новые способы производства оружия и возможность использования нейтрального статуса во благо своих граждан.
""".format(game_name, game_type)

        # Копируем текст в буфер обмена
        pyperclip.copy(text)

        print("Prompt added to the clipboard!\n")


if __name__ == "__main__":
    main()